#import "VerticalAlignmentLabel.h"

@implementation VerticalAlignmentLabel

- (id)initWithFrame:(CGRect)frame
{
    self = [super initWithFrame:frame];
    if (self) {
        // Initialization code
        self.verticalAlignment = VerticalAlignmentMiddle;
    }
    return self;
}

